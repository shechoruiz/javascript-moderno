// Creacion de funciones. Las funciones son basicamente lineas de codigo orientadas a realizar alguna labor particular, estas son reutilizables y permiten tener el codigo mas ordenado. Existen 2 formas de crear funciones en JavaScript:
// * Declaracion de funcion
// * Expresion de funcion

// Declaracion de funcion - function declaration
function sumar() {
  console.log(2+2)
}

sumar()

// Expresion de funcion - Function expression
const sumar2 = function(){
  console.log(2+3)
}

sumar2()
