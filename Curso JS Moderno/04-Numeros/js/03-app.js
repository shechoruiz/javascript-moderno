// El objeto Math: Objeto creado a nivel de consola, facilita el uso de funciones matematicas particulares

let resultado

// PI
resultado = Math.PI

// Redondear
resultado = Math.round(2.8)

// Redondear hacia arriba
resultado = Math.ceil(2.2)

// Redondear hacia abajo
resultado = Math.floor(2.7)

// Riaz cuadrada
resultado = Math.sqrt(144)

// Valor absoluto
resultado = Math.abs(-500)

// Potencia
resultado = Math.pow(2, 4)

// Minimo
resultado = Math.min(2, 4, -3, 1)

// Maximo
resultado = Math.max(2, 14, -3, 1)

